#If statement
#Conditionals use True or False

print("If = True")
if True:
    print("This prints if statement after 'if' is True")
else:
    print("This prints if statement is NOT True")

print("If = False")
if False:
    print("This prints if statement after 'if' is True")
else:
    print("This prints if statement is NOT True")


#Application for this if statement
someone_i_know = True

print("someone_i_know = True")
if someone_i_know:
    print("Hello! How are you?")
else:
    print("Nice to meet you!")


someone_i_know = False

print("someone_i_know = False")
if someone_i_know:
    print("Hello! How are you?")
else:
    print("Nice to meet you!")


someone_i_know = False

print("someone_i_know = False")
if someone_i_know:
    print("Hello! How are you?")
else:
    pass #This will not do anything
print("When False, the statement is 'pass'!")



